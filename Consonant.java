public class VowelConsonant {

    public static void main(String[] args) {

        char x = 'f';

        if(x == 'a' || x == 'e' || x == 'i' || x == 'o' || x == 'u'|| x == 'f' )
            System.out.println(x + " is Consonant");
        else
            System.out.println(x + " is vowel");

    }
}
